#ifndef __CONV_AD_TH_T_H
#define __CONV_AD_TH_T_H
#include <stdint.h>

/*******************************************************************************
* 将12位的AD采样值转换为温度(采用型号为HTG3500的温湿度传感器的温度阻值表)
* 入口参数：
*     val:  AD采样值
* 出口参数：
*     转换后的温度值( = 实际值*10, 范围：-300~700)
*******************************************************************************/
int16_t AD_Conv_TH_T(uint16_t val);

#endif
